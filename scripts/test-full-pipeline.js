const fs = require('fs');
const path = require('path');

// API Keys from environment
const ARENA_TOKEN = process.env.ARENA_TOKEN;
const REPLICATE_API_TOKEN = process.env.REPLICATE_API_TOKEN;
const IDEOGRAM_API_KEY = process.env.IDEOGRAM_API_KEY;

