const userBrief = process.argv.slice(2).join(' ') || 'A meditation app that feels calm, warm, and premium like floating in water at sunset';

const PROJECT_DIR = path.join(__dirname, '..', 'projects', `test-${Date.now()}`);

async function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function runCommand(name, fn) {
  console.log(`\n${'═'.repeat(60)}`);
  console.log(`🎯 ${name}`);
  console.log('═'.repeat(60));
  try {
    const result = await fn();
    console.log(`✅ ${name} complete`);
    return result;
  } catch (err) {
    console.error(`❌ ${name} failed:`, err.message);
    throw err;
  }
}

