// Step 6: Package everything
async function packageDeliverables(brief, channel, background, logo) {
  console.log('📦 Packaging deliverables...');
  
  const manifest = {
    project: {
      name: brief.appName,
      created: new Date().toISOString(),
      status: 'ready_for_review'
    },
    brief: brief,
    moodBoard: {
      url: `https://are.na/nathan-garvie/${channel.slug}`,
      channelId: channel.id
    },
    assets: {
      background: {
        type: 'flux-1.1-pro',
        path: background.path,
        url: background.url,
        prompt: background.prompt
      },
      logo: {
        type: 'ideogram-v3',
        path: logo.path,
        url: logo.url,
        prompt: logo.prompt
      }
    },
    nextSteps: [
      'Review mood board at Are.na link above',
      'Review generated assets in project folder',
      'Iterate or proceed to Figma assembly'
    ]
  };
  
  fs.writeFileSync(
    path.join(PROJECT_DIR, 'manifest.json'),
    JSON.stringify(manifest, null, 2)
  );
  
  return manifest;
}

// Main execution
async function main() {
  console.log('╔════════════════════════════════════════════════════════╗');
  console.log('║  AI Design Workflow - Full Pipeline Test               ║');
  console.log('╚════════════════════════════════════════════════════════╝\n');
  
  console.log(`📝 Brief: "${userBrief}"`);
  console.log(`📁 Output: ${PROJECT_DIR}`);
  
  try {
    // Step 1: Muse expands brief
    const brief = await runCommand('Muse: Expand Brief', () => museExpandBrief());
    
    // Step 2: Muse creates Are.na mood board
    const channel = await runCommand('Muse: Create Mood Board', () => museCreateMoodBoard(brief));
    
    // Step 3: Pixel generates background
    const background = await runCommand('Pixel: Generate Background (Flux)', () => pixelGenerateBackground(brief));
    
    // Step 4: Pixel generates logo
    const logo = await runCommand('Pixel: Generate Logo (Ideogram)', () => pixelGenerateLogo(brief));
    
    // Step 5: Pixel removes background
    await runCommand('Pixel: Remove Background', () => pixelRemoveBackground(logo.path));
    
    // Step 6: Package
    const manifest = await runCommand('Package Deliverables', () => packageDeliverables(brief, channel, background, logo));
    
    // Summary
    console.log('\n' + '═'.repeat(60));
    console.log('✅ PIPELINE COMPLETE');
    console.log('═'.repeat(60));
    console.log(`\n📁 Project: ${PROJECT_DIR}`);
    console.log(`\n🎨 Mood Board:`);
    console.log(`   ${manifest.moodBoard.url}`);
    console.log(`\n🖼️  Assets:`);
    console.log(`   Background: ${manifest.assets.background.path}`);
    console.log(`   Logo: ${manifest.assets.logo.path}`);
    console.log(`\n📋 Manifest: ${path.join(PROJECT_DIR, 'manifest.json')}`);
    console.log(`\n💰 Estimated cost: ~$0.80`);
    
  } catch (err) {
    console.error('\n❌ Pipeline failed:', err.message);
    process.exit(1);
  }
}

main();
