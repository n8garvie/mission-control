// Step 2: Muse creates Are.na mood board
async function museCreateMoodBoard(brief) {
  console.log('🎨 Muse creating Are.na mood board...');
  
  const channelName = `${brief.appName.toLowerCase()}-mood-${Date.now()}`;
  
  const response = await fetch('https://api.are.na/v2/channels', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${ARENA_TOKEN}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      title: `${brief.appName} — Mood Board`,
      slug: channelName,
      status: 'public',
      description: `AI Design Workflow test for ${brief.appName}\n\nStyle: ${brief.style}\nMood: ${brief.mood}\n\nGenerated by Muse.`
    })
  });
  
  if (!response.ok) {
    const error = await response.text();
    throw new Error(`Are.na API error: ${error}`);
  }
  
  const channel = await response.json();
  console.log(`   ✅ Created: https://are.na/nathan-garvie/${channel.slug}`);
  console.log(`   🆔 Channel ID: ${channel.id}`);
  
  // Save channel info
  fs.writeFileSync(
    path.join(PROJECT_DIR, 'arena-channel.json'),
    JSON.stringify(channel, null, 2)
  );
  
  return channel;
}

// Step 3: Pixel generates background with Flux
async function pixelGenerateBackground(brief) {
  console.log('🖼️  Pixel generating background with Flux 1.1 Pro...');
  console.log('   (This may take 10-30 seconds)...');
  
  const prompt = `A serene meditation app background, ${brief.mood}, ${brief.colorDirection}, soft gradients, organic flowing shapes, premium luxury feel, abstract water reflections, golden hour lighting, minimalist composition, 8k quality, suitable for mobile app background`;
  
  const response = await fetch('https://api.replicate.com/v1/models/black-forest-labs/flux-1.1-pro/predictions', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${REPLICATE_API_TOKEN}`,
      'Content-Type': 'application/json',
      'Prefer': 'wait' // Synchronous for simpler testing
    },
    body: JSON.stringify({
      input: {
        prompt: prompt,
        aspect_ratio: '9:16', // Mobile portrait
        output_format: 'png'
      }
    })
  });
  
  if (!response.ok) {
    const error = await response.text();
    throw new Error(`Replicate API error: ${error}`);
  }
  
  const prediction = await response.json();
  
  // If async, poll for result
  let result = prediction;
  if (prediction.status !== 'succeeded') {
    console.log('   ⏳ Waiting for generation (polling)...');
    result = await pollReplicateResult(prediction.id);
  }
  
  const imageUrl = result.output;
  console.log(`   ✅ Generated: ${imageUrl.substring(0, 60)}...`);
  
  // Download image
  const imageResponse = await fetch(imageUrl);
  const imageBuffer = Buffer.from(await imageResponse.arrayBuffer());
  const imagePath = path.join(PROJECT_DIR, 'background-flux.png');
  fs.writeFileSync(imagePath, imageBuffer);
  console.log(`   💾 Saved: ${imagePath}`);
  
  return { url: imageUrl, path: imagePath, prompt };
}

async function pollReplicateResult(predictionId) {
  for (let i = 0; i < 30; i++) {
    await sleep(2000);
    
    const response = await fetch(`https://api.replicate.com/v1/predictions/${predictionId}`, {
      headers: { 'Authorization': `Bearer ${REPLICATE_API_TOKEN}` }
    });
    
    const result = await response.json();
    
    if (result.status === 'succeeded') {
      return result;
    }
    if (result.status === 'failed') {
      throw new Error('Generation failed: ' + result.error);
    }
    
    process.stdout.write('.');
  }
  throw new Error('Timeout waiting for generation');
}

// Step 4: Pixel generates logo with Ideogram
async function pixelGenerateLogo(brief) {
  console.log('🔤 Pixel generating logo with Ideogram v3...');
  console.log('   (This may take 10-20 seconds)...');
  
  const prompt = `Minimalist logo design for "${brief.appName}" meditation app, clean typography, ${brief.colorDirection}, simple icon, zen aesthetic, professional brand identity, centered composition, white background`;
  
  const response = await fetch('https://api.ideogram.ai/v1/ideogram-v3/generate', {
    method: 'POST',
    headers: {
      'Api-Key': IDEOGRAM_API_KEY
    },
    body: new URLSearchParams({
      prompt: prompt,
      aspect_ratio: '1x1',
      style_type: 'DESIGN',
      rendering_speed: 'TURBO',
      num_images: '1'
    })
  });
  
  if (!response.ok) {
    const error = await response.text();
    throw new Error(`Ideogram API error: ${error}`);
  }
  
  const result = await response.json();
  const imageUrl = result.data[0].url;
  
  console.log(`   ✅ Generated: ${imageUrl.substring(0, 60)}...`);
  
  // Download image
  const imageResponse = await fetch(imageUrl);
  const imageBuffer = Buffer.from(await imageResponse.arrayBuffer());
  const imagePath = path.join(PROJECT_DIR, 'logo-ideogram.png');
  fs.writeFileSync(imagePath, imageBuffer);
  console.log(`   💾 Saved: ${imagePath}`);
  
  return { url: imageUrl, path: imagePath, prompt };
}

