#!/usr/bin/env node
/**
 * Threads Post Drafter
 * 
 * Analyzes saved designs and drafts posts in Nathan's style.
 * 
 * Usage: node draft-threads-post.js <image-path>
 * 
 * Nathan's style:
 * - Direct, no fluff
 * - No em dashes
 * - Focus on what makes design work (grid, type, interaction)
 * - Concise but insightful
 * - Questions to engage
 * - Occasionally humorous or sharp observations
 */

const fs = require('fs');
const path = require('path');

const SAVE_DIR = '/home/n8garvie/NateMate/notes/NateMateNotes/Agent Saved';
const DRAFTS_DIR = '/home/n8garvie/NateMate/notes/NateMateNotes/memory/content-drafts';

// Ensure drafts directory exists
fs.mkdirSync(DRAFTS_DIR, { recursive: true });

// Nathan's writing patterns (based on observed style)
const POST_TEMPLATES = {
  observation: [
    "The {aspect} here is perfect. {why}",
    "{designer} nailed the {element}. {specific_detail}",
    "This is how you do {technique}. {explanation}",
  ],
  question: [
    "Why does this {design_type} work so well? {answer}",
    "Ever notice how {observation}? This is a masterclass in that.",
    "What's your take on {trend}? This feels like the right way to do it.",
  ],
  breakdown: [
    "3 things working here:\n1. {point1}\n2. {point2}\n3. {point3}",
    "The hierarchy: {hierarchy_explanation}",
    "Grid is invisible but doing everything. {grid_detail}",
  ],
  appreciation: [
    "Clean. {elaboration}",
    "This restraint. {why_it_works}",
    "Minimal without being boring. {detail}",
  ],
  critique: [
    "Most {category} gets this wrong. {designer} didn't.",
    "The difference between good and great {element}. {explanation}",
  ]
};

const AESTHETIC_NOTES = {
  minimal: ["Restraint", "Negative space", "Single focal point", "No decoration"],
  typography: ["Type as image", "Hierarchy", "Readability", "Font pairing"],
  interaction: ["Micro-interactions", "Feedback loops", "Motion purpose", "States"],
  layout: ["Grid discipline", "Alignment", "White space", "Balance"],
  color: ["Restrained palette", "Contrast", "Emphasis", "Mood"]
};

async function analyzeImage(imagePath) {
  // In production, this would use vision API
  // For now, extract context from filename/path
  const basename = path.basename(imagePath, path.extname(imagePath));
  
  // Parse designer name from filename (threads-{designer}-{id} pattern)
  const match = basename.match(/^threads-(.+?)-[A-Z0-9]+$/);
  const designer = match ? match[1].replace(/-/g, ' ') : 'Unknown';
  
  return {
    designer,
    source: basename.startsWith('threads-') ? 'Threads' : 
            basename.startsWith('dribbble-') ? 'Dribbble' : 'Saved',
    filename: basename
  };
}

function generateDrafts(analysis) {
  const drafts = [];
  
  // Draft 1: Direct observation
  drafts.push({
    type: 'observation',
    text: `${analysis.designer} nailed the restraint here. No extra. Just the essential elements working together.`
  });
  
  // Draft 2: Question format
  drafts.push({
    type: 'question', 
    text: `Why does this feel so considered?\n\nThe spacing. The hierarchy. Nothing competing for attention.\n\nThis is what happens when you edit ruthlessly.`
  });
  
  // Draft 3: Breakdown
  drafts.push({
    type: 'breakdown',
    text: `3 things:\n\n1. Single focal point (no competition)\n2. Generous spacing (let it breathe)\n3. Type as structure (not just content)\n\nSimple but not easy.`
  });
  
  // Draft 4: Short & punchy
  drafts.push({
    type: 'short',
    text: `Clean hierarchy. Perfect spacing.\n\nThe basics, executed flawlessly.`
  });
  
  // Draft 5: Comparative/critical
  drafts.push({
    type: 'critique',
    text: `Most interfaces try to do too much.\n\nThis doesn't. And that's the whole point.`
  });
  
  return drafts;
}

function saveDrafts(imagePath, analysis, drafts) {
  const timestamp = new Date().toISOString().split('T')[0];
  const basename = path.basename(imagePath, path.extname(imagePath));
  const draftPath = path.join(DRAFTS_DIR, `${basename}-drafts.md`);
  
  const content = `# Content Drafts: ${analysis.designer}

**Source:** ${analysis.source}  
**Image:** ${path.basename(imagePath)}  
**Date:** ${timestamp}

---

## Draft Options

${drafts.map((d, i) => `### Option ${i + 1} (${d.type})

${d.text}

---
`).join('\n')}

## Notes

- Tone: Direct, no em dashes, focus on craft
- Audience: Design engineers, product designers
- Goal: Share appreciation for considered work
- Hashtags: #design #ui #ux #minimalism #designengineering

## To Post

1. Review drafts above
2. Pick one or remix
3. Add image from: ${path.relative(DRAFTS_DIR, imagePath)}
4. Post to Threads

---
*Auto-generated by Content Drafter*
`;
  
  fs.writeFileSync(draftPath, content);
  return draftPath;
}

async function main() {
  const imagePath = process.argv[2];
  
  if (!imagePath) {
    console.log('Usage: node draft-threads-post.js <image-path>');
    console.log('');
    console.log('Example:');
    console.log('  node draft-threads-post.js "/path/to/design.png"');
    process.exit(1);
  }
  
  if (!fs.existsSync(imagePath)) {
    console.error(`Image not found: ${imagePath}`);
    process.exit(1);
  }
  
  console.log('Analyzing image...');
  const analysis = await analyzeImage(imagePath);
  
  console.log(`Designer: ${analysis.designer}`);
  console.log(`Source: ${analysis.source}`);
  console.log('');
  
  console.log('Generating drafts...\n');
  const drafts = generateDrafts(analysis);
  
  drafts.forEach((d, i) => {
    console.log(`Option ${i + 1} (${d.type}):`);
    console.log(d.text);
    console.log('');
  });
  
  const draftPath = saveDrafts(imagePath, analysis, drafts);
  console.log(`✓ Saved to: ${draftPath}`);
}

main().catch(console.error);
