#!/usr/bin/env node
/**
 * Muse: Create mood board for a new project
 * Usage: node muse-create-mood-board.js "A meditation app that feels calm"
 */

const Anthropic = require('@anthropic-ai/sdk');
const fs = require('fs');
const path = require('path');

const ARENA_TOKEN = process.env.ARENA_TOKEN;
const ARENA_USER = process.env.ARENA_USER || 'nathan-garvie';

if (!ARENA_TOKEN) {
  console.error('❌ ARENA_TOKEN not set. Run: bash scripts/setup-api-keys.sh');
  process.exit(1);
}

const anthropic = new Anthropic();

async function expandBrief(userInput) {
  console.log('🧠 Muse: Expanding creative brief...\n');
  
  const response = await anthropic.messages.create({
    model: 'claude-sonnet-4-20250514',
    max_tokens: 1000,
    system: `You are a creative director. Given an app concept, generate a design brief.
             Return JSON with: appName, style, mood, emotions[], colorDirection, 
             targetAudience, searchQueries[] (5-7 specific search terms for mood boards).`,
    messages: [{ role: 'user', content: userInput }]
  });
  
  return JSON.parse(response.content[0].text);
}

async function createArenaChannel(brief) {
  console.log('🎨 Muse: Creating Are.na mood board...\n');
  
  const channelName = `${brief.appName.toLowerCase().replace(/\s+/g, '-')}-mood-board`;
  
  const response = await fetch('https://api.are.na/v2/channels', {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${ARENA_TOKEN}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({
      title: `${brief.appName} — Mood Board`,
      slug: channelName,
      status: 'public',
      description: `Inspiration for ${brief.appName}.\n\nStyle: ${brief.style}\nMood: ${brief.mood}\n\nAuto-generated by Muse.`
    })
  });
  
  if (!response.ok) {
    const error = await response.text();
    throw new Error(`Are.na API error: ${error}`);
  }
  
  return await response.json();
}

async function searchUnsplash(query, perPage = 5) {
  // Note: Unsplash requires an access key for production use
  // This is a simplified example - in production, use official API with key
  console.log(`  🔍 Searching: "${query}"`);
  
  // For demo purposes, return mock results
  // In real implementation, call: https://api.unsplash.com/search/photos
  return [
    { source: 'unsplash', query, note: 'Unsplash integration requires access key' }
  ];
}

async function addImagesToChannel(channelId, searchQueries) {
  console.log('\n📸 Muse: Gathering inspiration...\n');
  
  const allImages = [];
  
  for (const query of searchQueries.slice(0, 3)) { // Limit to 3 queries for demo
    const results = await searchUnsplash(query);
    allImages.push(...results);
    console.log(`  ✓ Found images for: "${query}"`);
  }
  
  // In full implementation, this would add actual images to Are.na
  console.log(`\n  📝 Would add ${allImages.length * 5} images to channel`);
  console.log('  (Full implementation requires Unsplash API key)');
  
  return allImages;
}

async function main() {
  const userInput = process.argv.slice(2).join(' ');
  
  if (!userInput) {
    console.log('Usage: node muse-create-mood-board.js "Your app idea"');
    console.log('');
    console.log('Example:');
    console.log('  node muse-create-mood-board.js "A meditation app that feels calm and premium"');
    process.exit(1);
  }
  
  console.log('╔════════════════════════════════════════════════════════╗');
  console.log('║  Muse: Creating Mood Board                             ║');
  console.log('╚════════════════════════════════════════════════════════╝\n');
  
  try {
    // Step 1: Expand brief
    const brief = await expandBrief(userInput);
    console.log('📋 Brief generated:');
    console.log(`   App: ${brief.appName}`);
    console.log(`   Style: ${brief.style}`);
    console.log(`   Mood: ${brief.mood}`);
    console.log(`   Search queries: ${brief.searchQueries.length} terms\n`);
    
    // Step 2: Create Are.na channel
    const channel = await createArenaChannel(brief);
    console.log('✓ Mood board created!\n');
    console.log(`   📎 URL: https://are.na/${ARENA_USER}/${channel.slug}`);
    console.log(`   🆔 Channel ID: ${channel.id}\n`);
    
    // Step 3: Gather inspiration (demo)
    await addImagesToChannel(channel.id, brief.searchQueries);
    
    // Summary
    console.log('\n─────────────────────────────────────────────────────────\n');
    console.log('✓ Mood board ready for review!\n');
    console.log(`🔗 View: https://are.na/${ARENA_USER}/${channel.slug}`);
    console.log('\nNext steps:');
    console.log('  1. Browse the mood board at the link above');
    console.log('  2. Manually curate/add images via Are.na web/app');
    console.log('  3. When ready, tell Muse: "Proceed with generation"');
    console.log('\nOr run Pixel directly:');
    console.log(`  node pixel-generate-assets.js ${channel.id} '${JSON.stringify(brief).replace(/'/g, "\\'")}'`);
    
    // Save to project file
    const projectDir = path.join(__dirname, '..', 'projects', channel.slug);
    fs.mkdirSync(projectDir, { recursive: true });
    
    fs.writeFileSync(
      path.join(projectDir, 'brief.json'),
      JSON.stringify({ ...brief, channelId: channel.id, channelUrl: `https://are.na/${ARENA_USER}/${channel.slug}` }, null, 2)
    );
    
    console.log(`\n💾 Project saved to: ${projectDir}`);
    
  } catch (err) {
    console.error('\n❌ Error:', err.message);
    process.exit(1);
  }
}

main();
